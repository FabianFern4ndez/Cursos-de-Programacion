#Multiplicación *  División /   , la division siempre arroja un numero flotante
print()
print(2*3)
print(6/3)

#Division entera //, redondea la parte decimal pero aun asi puede arrojar flotantes si una entrada lo es
print()
print(6//3)
print(6.0//3)
print(5//2) #fijate que aqui da 2 entero
print(5.0//2)
#el redonde de esta division siempre va hacia abajo
print(-6 // 4)
print(6. // -4)

#Residuo %
print()
print(16%5)  #16 entre 5 = 16-5(3) = 1

